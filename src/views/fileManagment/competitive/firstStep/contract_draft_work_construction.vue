            <div style="text-align: justify;"><span style="margin-left: 25px;"/>6.2 Սույն պայմանագրով նախատեսված Աշխատանքի կատարման ժամկետը խախտելու դեպքում Կապալառուից յուրաքանչյուր ուշացված աշխատանքային օրվա համար գանձվում է տույժ` կատարման ենթակա, սակայն չկատարված Աշխատանքի գնի 0,05 (զրո ամբողջ հինգ հարյուրերրորդական) տոկոսի չափով։</div>
