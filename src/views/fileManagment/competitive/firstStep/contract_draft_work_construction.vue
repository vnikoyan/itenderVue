            <div style="text-align: justify;"><span style="margin-left: 25px;"/>6.4 Պայմանագրի 6.2 և 6.3 կետերով նախատեսված տույժը և տուգանքը հաշվարկվում և հաշվանցվում են  Կապալառուին վճարվող գումարների հետ։</div>
