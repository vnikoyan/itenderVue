            <span style="text-align: justify;" v-else><span style="margin-left: 25px;"/>6.3. Պայմանագրի N 1 հավելվածում նշված տեխնիկական բնութագրին չհամապատասխանող ծառայություն մատուցելու յուրաքանչյուր դեպքում Կատարողից գանձվում է տուգանք` պայմանագրի 5.1 կետում նախատեսված գումարի 0,5 (զրո ամբողջ հինգ տասնորդական) տոկոսի չափով: Եթե պայմանագիրը կնքվել է «Գնումների մասին» ՀՀ օրենքի 15-րդ հոդվածի 6-րդ կետի հիման վրա, ապա տուգանքը հաշվարկվում է այն համաձայնագրի գնի նկատմամբ, որի շրջանակում արձանագրվել է ստանձնված պարտավորությունների չկատարման կամ ոչ պատշաճ կատարման հանգամանքը: Եթե պայմանագիրը ներառում է մեկից ավել չափաբաժին, ապա տուգանքը հաշվարկվում է պայմանագրով այդ չափաբաժնի համար սահմանված ընդհանուր գնի նկատմամբ:</span>
